package com.drinkti.model;

public class Drink {
    private String mbti;
    private String name;
    private String enName;
    private String color;
    private String slogan;
    private String description;
    private String tagsJson;
    private String glassShape;

    public String getMbti() { return mbti; }
    public void setMbti(String mbti) { this.mbti = mbti; }
    public String getName() { return name; }
    public void setName(String name) { this.name = name; }
    public String getEnName() { return enName; }
    public void setEnName(String enName) { this.enName = enName; }
    public String getColor() { return color; }
    public void setColor(String color) { this.color = color; }
    public String getSlogan() { return slogan; }
    public void setSlogan(String slogan) { this.slogan = slogan; }
    public String getDescription() { return description; }
    public void setDescription(String description) { this.description = description; }
    public String getTagsJson() { return tagsJson; }
    public void setTagsJson(String tagsJson) { this.tagsJson = tagsJson; }
    public String getGlassShape() { return glassShape; }
    public void setGlassShape(String glassShape) { this.glassShape = glassShape; }
}
