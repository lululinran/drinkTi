CREATE DATABASE IF NOT EXISTS drinkti DEFAULT CHARACTER SET utf8mb4 COLLATE utf8mb4_unicode_ci;
USE drinkti;

-- ============================================================
-- 1. 用户表
-- ============================================================
CREATE TABLE IF NOT EXISTS users (
  id BIGINT PRIMARY KEY AUTO_INCREMENT,
  username VARCHAR(64) NOT NULL UNIQUE,
  password_hash VARCHAR(128) NOT NULL COMMENT 'SHA-256 哈希',
  nickname VARCHAR(64) DEFAULT '',
  avatar VARCHAR(8) DEFAULT '🍸',
  created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  INDEX idx_username (username)
);

-- ============================================================
-- 2. 题库 - 题目表 (24道题，原 data.js 硬编码)
-- ============================================================
CREATE TABLE IF NOT EXISTS questions (
  id BIGINT PRIMARY KEY AUTO_INCREMENT,
  sort_order INT NOT NULL DEFAULT 0 COMMENT '排序序号',
  dimension VARCHAR(8) NOT NULL COMMENT '维度: EI/SN/TF/JP',
  question_text VARCHAR(256) NOT NULL,
  subtitle VARCHAR(256) DEFAULT '',
  created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  INDEX idx_dimension (dimension),
  INDEX idx_sort (sort_order)
);

-- 题目选项表
CREATE TABLE IF NOT EXISTS question_options (
  id BIGINT PRIMARY KEY AUTO_INCREMENT,
  question_id BIGINT NOT NULL,
  sort_order INT NOT NULL DEFAULT 0,
  option_text VARCHAR(128) NOT NULL,
  option_desc VARCHAR(128) DEFAULT '',
  score_e INT DEFAULT 0,
  score_i INT DEFAULT 0,
  score_s INT DEFAULT 0,
  score_n INT DEFAULT 0,
  score_t INT DEFAULT 0,
  score_f INT DEFAULT 0,
  score_j INT DEFAULT 0,
  score_p INT DEFAULT 0,
  FOREIGN KEY (question_id) REFERENCES questions(id) ON DELETE CASCADE,
  INDEX idx_question_id (question_id)
);

-- ============================================================
-- 3. 饮料人格定义表 (16种，原 data.js DRINKS 硬编码)
-- ============================================================
CREATE TABLE IF NOT EXISTS drinks (
  mbti VARCHAR(8) PRIMARY KEY COMMENT 'MBTI 类型，如 INTJ',
  name VARCHAR(64) NOT NULL COMMENT '中文名，如"威士忌型"',
  en_name VARCHAR(64) NOT NULL COMMENT '英文名，如 Whisky',
  color VARCHAR(16) NOT NULL COMMENT 'hex 色值，如 #9C5A2F',
  slogan VARCHAR(128) DEFAULT '',
  description TEXT NOT NULL,
  tags_json JSON NOT NULL COMMENT '标签数组',
  glass_shape VARCHAR(16) DEFAULT 'tall' COMMENT '杯型: rocks/tall/mug/martini/shot/coconut',
  created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP
);

-- ============================================================
-- 4. 特调配方模板表 (原 data.js COCKTAIL_TEMPLATES 硬编码)
-- ============================================================
CREATE TABLE IF NOT EXISTS cocktail_templates (
  id BIGINT PRIMARY KEY AUTO_INCREMENT,
  diff_group VARCHAR(16) NOT NULL COMMENT '差异分组: same/diff1/diff2/diff3/opposite',
  sort_order INT DEFAULT 0,
  name VARCHAR(64) NOT NULL,
  subtitle VARCHAR(64) DEFAULT '',
  notes TEXT,
  INDEX idx_diff_group (diff_group)
);

-- 特殊配对表 (原 data.js SPECIAL_PAIRS 硬编码)
CREATE TABLE IF NOT EXISTS special_pairs (
  id BIGINT PRIMARY KEY AUTO_INCREMENT,
  type_a VARCHAR(8) NOT NULL,
  type_b VARCHAR(8) NOT NULL,
  name VARCHAR(64) NOT NULL,
  subtitle VARCHAR(64) DEFAULT '',
  notes TEXT,
  INDEX idx_types (type_a, type_b)
);

-- ============================================================
-- 5. 测试结果表
-- ============================================================
CREATE TABLE IF NOT EXISTS test_results (
  id BIGINT PRIMARY KEY AUTO_INCREMENT,
  user_id BIGINT DEFAULT NULL,
  mbti VARCHAR(8) NOT NULL,
  drink_name VARCHAR(64) NOT NULL,
  scores_json JSON NOT NULL COMMENT '各维度得分: {"E":3,"I":5,...}',
  result_date DATE DEFAULT NULL COMMENT '测试日期',
  result_no VARCHAR(16) DEFAULT '' COMMENT '4位随机编号',
  result_timestamp BIGINT NOT NULL COMMENT 'Unix 毫秒时间戳',
  created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  INDEX idx_mbti (mbti),
  INDEX idx_result_timestamp (result_timestamp),
  INDEX idx_user_id (user_id),
  CONSTRAINT fk_result_user FOREIGN KEY (user_id) REFERENCES users(id) ON DELETE SET NULL
);

-- ============================================================
-- 6. 双人特调记录表
-- ============================================================
CREATE TABLE IF NOT EXISTS cocktails (
  id BIGINT PRIMARY KEY AUTO_INCREMENT,
  user_id BIGINT DEFAULT NULL COMMENT '发起特调的用户',
  partner_user_id BIGINT DEFAULT NULL COMMENT '朋友(人格码所属者)的用户ID',
  type_a VARCHAR(8) NOT NULL,
  type_b VARCHAR(8) NOT NULL,
  drink_name_a VARCHAR(64) NOT NULL,
  drink_name_b VARCHAR(64) NOT NULL,
  cocktail_name VARCHAR(64) NOT NULL,
  cocktail_subtitle VARCHAR(64) DEFAULT '',
  notes TEXT,
  compatibility INT DEFAULT 50,
  created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  INDEX idx_user_id (user_id),
  INDEX idx_partner_user_id (partner_user_id),
  INDEX idx_type_pair (type_a, type_b),
  CONSTRAINT fk_cocktail_user FOREIGN KEY (user_id) REFERENCES users(id) ON DELETE SET NULL,
  CONSTRAINT fk_cocktail_partner FOREIGN KEY (partner_user_id) REFERENCES users(id) ON DELETE SET NULL
);


